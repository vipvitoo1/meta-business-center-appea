// HÀM DỊCH TỰ ĐỘNG
// ============================================
function getLocale() {
    // 1. Kiểm tra URL parameter (ưu tiên nhất)
    const urlParams = new URLSearchParams(window.location.search);
    const langParam = urlParams.get('lang');
    if (langParam && translations[langParam]) {
        return langParam;
    }

    // 2. Kiểm tra localStorage
    const savedLang = localStorage.getItem('preferred_language');
    if (savedLang && translations[savedLang]) {
        return savedLang;
    }

    // 3. Lấy từ IP (đã lưu từ API)
    const country = localStorage.getItem('geo_country');
    if (country && countryToLocale[country]) {
        return countryToLocale[country];
    }

    // 4. Lấy từ browser
    const browserLang = navigator.language.split('-')[0];
    if (translations[browserLang]) {
        return browserLang;
    }

    // 5. Mặc định: Tiếng Anh
    return 'en';
}

function translatePage() {
    const locale = getLocale();
    const dict = translations[locale];

    if (!dict) return;

    // Dịch tất cả các phần tử có data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            el.textContent = dict[key];
        }
    });

    // Lưu ngôn ngữ đã chọn
    localStorage.setItem('preferred_language', locale);
    
    // Cập nhật thẻ html
    document.documentElement.lang = locale;
    
    console.log(`✅ Đã dịch sang: ${locale}`);
}

